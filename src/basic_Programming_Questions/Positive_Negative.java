package basic_Programming_Questions;

public class Positive_Negative {

	public static void main(String[] args) {
		double num = 12.32;
		if (num > 0.0) {
			System.out.println(num + " Is positive");
		} else if (num < 0.0) {
			System.out.println(num + " Is negative");
		} else {
			System.out.println(num + " Is zero");
		}
	}

}
